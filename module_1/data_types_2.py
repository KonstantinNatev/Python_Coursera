# Numeric
# int
a = 10
# float
b = 1.2
# complex
c = a + b * 1j

# Sequence
# str
d = "sequence"
# list
e = ["a", "b", "c", "d"]
# tuple
f = (1, 2, 3)

# Dictionary
g = {a: 1, b: 2, c: 3, d: 4}

# Boolean
h = False

# Set
i = {1, 1, 2, 3}

print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))
print(type(f))
print(type(g))
print(type(h))
print(type(i))
