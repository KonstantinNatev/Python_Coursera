# Math operators
# additional operator
a = 2 + 3

# subtraction operator
b = 4 - 5

# multiplication operator
c = 5 * 4

# division operator
d = 6 / 2

# Logical operators
e = 2
f = 9

# and
g = 1 < e and f < 10  # true && true

# or
h = 1 < e or f < 10  # true || true

# not
i = not (e < 5)  # !true => false
